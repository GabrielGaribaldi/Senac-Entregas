
// Perguntar ao usuário sua idade em anos.
const idade = Number(prompt("Qual sua idade?"))

// Calcular e imprimir no console a idade do usuário em meses.
const idadeMeses = idade * 12
console.log("Sua idade em meses é:", idadeMeses)

// Calcular e imprimir no console a idade do usuário em dias.
const idadeDias = idade * 365
console.log("Sua idade em dias é:", idadeDias)

// Calcular e imprimir no console a idade do usuário em horas.
const idadeHoras = idadeDias * 24
console.log("Sua idade em horas é:", idadeHoras)