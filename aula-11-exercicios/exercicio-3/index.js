
// Declarar a função 
function calcularSoma (a, b) {

    // Calcular a soma
    const soma = a + b

    // Retornar a soma dos resultados
    return soma
}


// Imprimir no console
console.log(calcularSoma(1,100))
console.log(calcularSoma(10,10))
console.log(calcularSoma(1000,10))


