
// ARRAYS DE OBJETOS

const usuarios = [ 
    {
        nome: 'José', 
        idade: 89
    },
    {
        nome: 'Maria', 
        idade: 76 
    },
]

// Acessar dados na primeira posição do array
console.log(usuarios[0].nome)
console.log(usuarios[0].idade)

// Acessar dados na segunda posição do array
console.log(usuarios[1].nome)
console.log(usuarios[1].idade)

// Acessar dados na ultima posição do array
console.log(usuarios[usuarios.length - 1].nome)
console.log(usuarios[usuarios.length - 1].idade)


